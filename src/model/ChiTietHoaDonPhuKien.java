package model;

public class ChiTietHoaDonPhuKien {
    int mapk;
    String tenpk;
    int giaban;
    int phamtramgiam;
    int soluong;

    public int getMapk() {
        return mapk;
    }

    public void setMapk(int mapk) {
        this.mapk = mapk;
    }

    public String getTenpk() {
        return tenpk;
    }

    public void setTenpk(String tenpk) {
        this.tenpk = tenpk;
    }

    public int getGiaban() {
        return giaban;
    }

    public void setGiaban(int giaban) {
        this.giaban = giaban;
    }

    public int getPhamtramgiam() {
        return phamtramgiam;
    }

    public void setPhamtramgiam(int phamtramgiam) {
        this.phamtramgiam = phamtramgiam;
    }

    public int getSoluong() {
        return soluong;
    }

    public void setSoluong(int soluong) {
        this.soluong = soluong;
    }
}
