package model;

public class PhuKien {
    private int mapk;
    private String tenpk;
    private int loaipk;
    private int soluong;
    private int giaban;
    private int phantramgiam;
    private int mansx;

    public int getMapk() {
        return mapk;
    }

    public void setMapk(int mapk) {
        this.mapk = mapk;
    }

    public String getTenpk() {
        return tenpk;
    }

    public void setTenpk(String tenpk) {
        this.tenpk = tenpk;
    }

    public int getLoaipk() {
        return loaipk;
    }

    public void setLoaipk(int loaipk) {
        this.loaipk = loaipk;
    }

    public int getSoluong() {
        return soluong;
    }

    public void setSoluong(int soluong) {
        this.soluong = soluong;
    }

    public int getGiaban() {
        return giaban;
    }

    public void setGiaban(int giaban) {
        this.giaban = giaban;
    }

    public int getPhantramgiam() {
        return phantramgiam;
    }

    public void setPhantramgiam(int phantramgiam) {
        this.phantramgiam = phantramgiam;
    }

    public int getMansx() {
        return mansx;
    }

    public void setMansx(int mansx) {
        this.mansx = mansx;
    }
}
